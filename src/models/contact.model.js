import mongoose from "mongoose";
const { Schema, models, model } = mongoose;

const contactSchema = new Schema({
  fullName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  subject: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
});

const Contact = models.Contact || model("Contact",contactSchema)
export default Contact